    llm = ChatOpenAI(model="gpt-4o-mini", temperature=0)

    # ë‹¨ê³„ 8: ì²´ì¸(Chain) ìƒì„±
    chain = (
        {"context": retriever, "question": RunnablePassthrough()}
        | prompt
        | llm
        | StrOutputParser()
    )

    return chain


